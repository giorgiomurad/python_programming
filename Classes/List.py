# List Functions

# 1. [list].append(item)
# Adds the argument as a new element to the list.



# 2. [list].index(item)
# Returns the index of the first element that is equal to the item.
# Return -1 if no element is equal to the item.



# 3. [list].insert(index, item)
# Inserts the item in the specified index.



# 4. [list].sort()
# Sorts the elements on the list so that they would appear in ascending order.



# 5. [list].remove(item)
# Removes the first element that is equal to the given item.



# 6. [list].reverse()
# Reverses the order of the elements in the list.